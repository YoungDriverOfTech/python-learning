# Dic is like map in java
# Create dictionary
demo_dict = {
    "aa": "AA",
    "bb": "BB",
    "cc": "CC"
}

# Printout dict
print(demo_dict)
print(len(demo_dict))

# Get value
print(demo_dict["aa"])

# Delete element
del demo_dict["aa"]
print(demo_dict)

# Update element
demo_dict["bb"] = "BBB"
print(demo_dict)